static bool SplitData2TwoParts(const std::string& strData, std::string& strTitle, std::string& strCommaPart)
{
	//fmt:			title:c1,n1,c2,n2
	int pColon = strData.find(":");
	if (pColon == std::string::npos)
	{
		return false;
	}
	
	strTitle = strData.substr(0, pColon);
	if (pColon < strData.size() - 1)
	{
		strCommaPart = strData.substr(pColon + 1);
	}
	
	return true;
}

static bool SplitCommas(const std::string& strRecvContent, StringVec_t& stringVec)
{
	return SplitStr(strRecvContent, ",", stringVec);
}


bool CContactInfoCollectionUtils::MakeCommaPartItemVec(const std::string& commaPart, ItemVec_t& itemVec)
{
	StringVec_t strVec;
	bool bRet = SplitCommas(commaPart, strVec);
	if (!bRet)
		return false;
	
	int nCnt = (int)strVec.size();
	if (nCnt % 2 != 0)
	{
		//c1,n1,c2,n2 .... must be pairs
		return false;
	}
	
	for (int i = 0; i < nCnt; ++i)
	{
		Item item;
		item.m_strCode = strVec[i];
		item.m_strName = strVec[i + 1];
		
		itemVec.push_back(item);
		
		++i;
	}
	
	return true;
}

bool CContactInfoCollectionUtils::InsertData2ItemMap(const std::string& strData, ItemMap_t& itemMap)
{
	std::string strTitle;
	std::string strCommaPart;
	
	bool bRet = SplitData2TwoParts(strData, strTitle, strCommaPart);
	if (!bRet)
		return false;
	
	ItemVec_t itemVec;
	bRet = MakeCommaPartItemVec(strCommaPart, itemVec);
	if (!bRet)
		return false;
	
	itemMap.insert(ItemMap_t::value_type(strTitle, itemVec));
	
	return true;
}

