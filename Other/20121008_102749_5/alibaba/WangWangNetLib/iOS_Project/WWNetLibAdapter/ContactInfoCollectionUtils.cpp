static int code_convert(char *from_charset,char *to_charset,char *inbuf,int inlen,char *outbuf,int outlen)
{
	iconv_t cd;
	//int rc;
	char **pin = &inbuf;
	char **pout = &outbuf;
	
	cd = iconv_open(to_charset,from_charset);
	if (cd==0)
	{
#ifdef DEBUG
		printf("iconv_open failed!\n");
#endif
		return -1;
	}
	memset(outbuf,0,outlen);
	if (iconv(cd,pin,(size_t*)&inlen,pout,(size_t*)&outlen)==-1)
	{
		//printf("iconv failed!\n");
		return -1;
	}
	
	iconv_close(cd);
	return 0;
}


static int u2g(char *inbuf,int inlen,char *outbuf,int outlen)
{
	return code_convert((char*)"utf-8",(char*)"gbk",inbuf,inlen,outbuf,outlen);
}

static void Utf8_2_GBK(const std::string& strUtf8Content, std::string& strGbkContent)
{
	int nUtf8Content = (int)strUtf8Content.length();
	
	//utf-8编码的长度肯定比gbk的长，因此gbk的字符串缓冲区设置成和utf-8一样长应该足够了
	char *pBuf = new char[nUtf8Content + 1];
	memset(pBuf, 0, nUtf8Content + 1);
	
	u2g((char*)strUtf8Content.data(), nUtf8Content, pBuf, nUtf8Content);
	
	strGbkContent = pBuf;
	
	delete []pBuf;
}

std::string CContactInfoCollectionUtils::GetNameByCode(const std::string& strTitle, const std::string& strCode, const ItemMap_t& itemMap)
{
	std::string strRet = "";
	
	if (strTitle.empty() || strCode.empty())
		return strRet;
	
	ItemMap_t::const_iterator p = itemMap.find(strTitle);
	if (p == m_infoMap.end())
		return strRet;
	
	const ItemVec_t& itemVec = p->second;
	
	ItemVec_t::const_iterator b = itemVec.begin();
	ItemVec_t::const_iterator e = itemVec.end();
	for (; b != e; ++b)
	{
		const Item& item = *b;
		if (item.m_strCode == strCode)
			return item.m_strName;
	}
	
	
	return strRet;
}


std::string CContactInfoCollectionUtils::GetNameByCodeInNormalInfoMap(const std::string& strKey, const std::string& strCode)
{
	std::string strUtf8Ret = GetNameByCode(strKey, strCode, m_infoMap);
	
	std::string strRet;
	Utf8_2_GBK(strUtf8Ret, strRet);
	
	return strRet;
   
	
}

std::string CContactInfoCollectionUtils::GetNameByCodeInAreaMap(const std::string& strParentCode, const std::string& strCode)
{
	std::string strUtf8Ret = GetNameByCode(strParentCode, strCode, m_areaMap);
	std::string strRet;
	
	Utf8_2_GBK(strUtf8Ret, strRet);
	
	return strRet;
}


