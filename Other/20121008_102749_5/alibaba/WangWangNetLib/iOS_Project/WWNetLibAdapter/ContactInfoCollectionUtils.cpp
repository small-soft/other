typedef std::vector<std::string> StringVec_t;


static bool SplitStr(const std::string& strRecvContent, const std::string& strSeperator, StringVec_t& stringVec)
{
	//fmt:                    a, b, c, d
	
	int b = 0;
	
	std::string strTmpContent = strRecvContent;
	
	while (1) {
	    if (strTmpContent.empty())
			break;
		
	    int p = strTmpContent.find(strSeperator);
	    if (p == std::string::npos)
	    {
			std::string strLeft = strTmpContent.substr(b);
			stringVec.push_back(strLeft);
			break;
	    }
		
	    std::string strValue = strTmpContent.substr(b, p - b);
	    stringVec.push_back(strValue);
		
	    strTmpContent = strTmpContent.substr(p + 1);
	}
	
	return true;
}

bool CContactInfoCollectionUtils::InitItemMap(const std::string& strData, ItemMap_t& itemMap)
{
    
	StringVec_t strVec;
	bool bRet = SplitStr(strData, "\n", strVec);
	if (!bRet)
		return false;
	
	StringVec_t::const_iterator b = strVec.begin();
	StringVec_t::const_iterator e = strVec.end();
	for (; b != e; ++b)
	{
		const std::string& strOneRecord = *b;
		InsertData2ItemMap(strOneRecord, itemMap);
	}
	
	return true;
}


