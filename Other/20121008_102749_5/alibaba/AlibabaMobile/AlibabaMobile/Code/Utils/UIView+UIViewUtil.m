#import "UIView+UIViewUtil.h"

@implementation UIView (UIViewUtil)

// view的结束坐标
-(CGFloat)endX {
    return  self.frame.origin.x + self.frame.size.width;
}

// view的结束坐标
-(CGFloat)endY {
    return  self.frame.origin.y + self.frame.size.height;
}
@end
